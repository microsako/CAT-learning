"""
DKT-PyTorch 配置文件

本配置文件包含模型训练所需的所有超参数，可根据硬件配置和需求进行调整。
"""

import torch

# ============================================================================
# 路径配置
# ============================================================================
class Paths:
    # 数据集路径
    TRAIN_DATA = "data/assistments/builder_train.csv"
    TEST_DATA = "data/assistments/builder_test.csv"

    # 模型输出目录
    OUTPUT_DIR = "output/"
    MODEL_DIR = "output/models/"
    LOG_DIR = "output/logs/"


# ============================================================================
# 模型超参数
# ============================================================================
class ModelConfig:
    # 隐藏层神经元数量
    # 值越大，模型表达能力越强，但训练时间越长，显存占用越高
    # 原始论文建议: 100-500
    N_HIDDEN = 200

    # 循环网络层数
    # 单层 RNN 通常足够，多层可以学习更复杂的模式但训练更慢
    N_LAYERS = 1

    # 是否使用 LSTM (替代 Basic RNN)
    # LSTM 可以更好地处理长序列的梯度消失问题
    USE_LSTM = True

    # Dropout 概率
    # 防止过拟合，建议值: 0.0 - 0.5
    # 训练集较小时可以适当提高
    DROPOUT = 0.5


# ============================================================================
# 训练超参数
# ============================================================================
class TrainConfig:
    # 批次大小
    # 值越大，梯度估计越稳定，但显存占用越高
    # 建议: 根据显存大小设置，8-128 之间
    BATCH_SIZE = 32

    # 初始学习率
    # 值越大，收敛越快，但可能不稳定
    # 值越小，收敛越慢，但更稳定
    LEARNING_RATE = 0.1

    # 学习率衰减系数
    # 每 epoch 后学习率 = 学习率 * 衰减系数
    LEARNING_RATE_DECAY = 0.95

    # 梯度裁剪阈值
    # 防止梯度爆炸，建议值: 1.0 - 10.0
    # 如果训练时打印 "梯度爆炸警告"，请减小此值
    MAX_GRAD_NORM = 5.0

    # 训练轮数
    # 根据验证集表现调整，过多会导致过拟合
    EPOCHS = 50

    # 早停耐心值
    # 如果验证集性能连续 N 轮没有提升，则停止训练
    EARLY_STOPPING_PATIENCE = 10


# ============================================================================
# 数据处理配置
# ============================================================================
class DataConfig:
    # 序列最大长度
    # 超过此长度的序列会被截断
    # 设为 None 则使用数据集中的最大长度
    MAX_SEQ_LEN = None

    # 题目数量
    # ASSISTments 数据集有 110 道题
    # 如果使用其他数据集，需要修改此值
    N_QUESTIONS = 110

    # 输入特征维度
    # = 题目数量 * 2 (题目ID + 答题正确性)
    # 例如: 110 * 2 = 220
    INPUT_DIM = ModelConfig.N_HIDDEN  # 压缩感知后的维度，可调

    # 是否使用压缩感知 (Compressed Sensing)
    # 将高维输入压缩到低维，可以加速训练
    USE_COMPRESSED_SENSING = False
    COMPRESSED_DIM = 100


# ============================================================================
# 设备配置
# ============================================================================
class DeviceConfig:
    # 计算设备
    # "cuda" 使用 GPU (需要 NVIDIA 显卡 + CUDA)
    # "mps" 使用 Apple Silicon GPU (M1/M2/M3 芯片)
    # "cpu" 使用 CPU
    DEVICE = "mps" if torch.backends.mps.is_available() else "cuda" if torch.cuda.is_available() else "cpu"

    # 随机种子
    # 设置固定值保证实验可复现
    RANDOM_SEED = 42


# ============================================================================
# 评估指标配置
# ============================================================================
class EvalConfig:
    # 计算 AUC 的阈值数量
    AUC_STEPS = 100


# ============================================================================
# 便捷访问函数
# ============================================================================
def get_device():
    """获取计算设备"""
    return DeviceConfig.DEVICE


def get_all_configs():
    """获取所有配置"""
    return {
        "paths": Paths,
        "model": ModelConfig,
        "train": TrainConfig,
        "data": DataConfig,
        "device": DeviceConfig,
        "eval": EvalConfig,
    }
